import pandas as pd
import numpy as np

np.random.seed(42)

n = 500

synthetic_data = pd.DataFrame({

    # Basic info
    'age': np.random.randint(16, 35, n),

    'height (in cm)': np.random.randint(150, 195, n),

    'weight (in kg)': np.random.randint(45, 110, n),

    # Training workload
    'no. of uchikomi sessions/week':
        np.random.randint(1, 8, n),

    'no. of nagekomi sessions/week':
        np.random.randint(1, 7, n),

    'no. of randori sessions/week':
        np.random.randint(1, 6, n),

    'avg randori duration (minutes)':
        np.random.randint(10, 60, n),

    'no. of strength training session/week':
        np.random.randint(0, 6, n),

    # Fatigue / intensity
    'training intensity':
        np.random.randint(1, 11, n),

    'grip fatigue level':
        np.random.randint(1, 11, n),

    'lower body fatigue':
        np.random.randint(1, 11, n),

    'breakfall (ukemi) proficiency':
        np.random.randint(1, 11, n),

    # Competition exposure
    'no. of competitions played in last 6 months':
        np.random.randint(0, 15, n),

    # Weight cut
    'avg weight cut in kgs':
        np.round(np.random.uniform(0, 8, n), 1),

    # Recovery habits
    'regular ice baths':
        np.random.choice([0,1], n),

    'physio sessions':
        np.random.choice([0,1], n),

    'do you feel exhausted after randori?':
        np.random.choice([0,1], n),

    'do you resist throws instead of falling?':
        np.random.choice([0,1], n),

    'train under coach supervision':
        np.random.choice([0,1], n),

    'weight cuts done?':
        np.random.choice([0,1], n)
})

# Create injury probability logic

injury_score = (

    synthetic_data['training intensity'] * 0.25 +

    synthetic_data['grip fatigue level'] * 0.20 +

    synthetic_data['lower body fatigue'] * 0.20 +

    synthetic_data['avg weight cut in kgs'] * 0.15 +

    synthetic_data['no. of competitions played in last 6 months'] * 0.10 +

    synthetic_data['avg randori duration (minutes)'] * 0.02 +

    synthetic_data['do you feel exhausted after randori?'] * 1.5 +

    synthetic_data['do you resist throws instead of falling?'] * 1.2 -

    synthetic_data['regular ice baths'] * 0.8 -

    synthetic_data['physio sessions'] * 1.0 -

    synthetic_data['train under coach supervision'] * 0.7

)

# Convert score to injury label

threshold = injury_score.median()

synthetic_data['injury in last 12 months'] = (
    injury_score > threshold
).astype(int)

# View data

print(synthetic_data.head())

synthetic_data.to_csv(
    "synthetic_athlete_dataset.csv",
    index=False
)

